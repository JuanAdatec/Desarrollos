	PAGO_MAST PM WITH(NOLOCK) INNER JOIN VENDEDORES V WITH(NOLOCK) ON
		PM.VENDEDOR_CODIGO = V.VENDEDOR_CODIGO
	INNER JOIN PAGO_ITEM PI WITH(NOLOCK) ON
		PM.PAGO_CODIGO = PI.PAGO_CODIGO
