		AND SA.f353_consec_docto_cruce = RIGHT(PI.NUMERO_DOCUMENTO, LEN(PI.NUMERO_DOCUMENTO) - PATINDEX ('%-%' , PI.NUMERO_DOCUMENTO))
		AND SA.F353_rowid_tercero = C.f201_rowid_tercero
